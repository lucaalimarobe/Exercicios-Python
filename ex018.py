''' Seno, Cosseno, Tangente'''
 
import math

radiante = math.radians(float(input('Digite o angulo que voce deseja: ')))
print(f'O angulo  de {radiante} tem o SENO de {math.sin(radiante):.2f}')
print(f'O angulo  de {radiante} tem o COSSENO de {math.cos(radiante):.2f}')
print(f'O angulo  de {radiante} tem o TANGENTE DE {math.tan(radiante):.2f}')